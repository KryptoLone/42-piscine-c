void	ft_putchar(char c);

void	rush(int x, int y)
{
	int l;
	int h;

	l = 1;
	h = 1;
	while (y >= h)
	{
		while (x >= l)
		{
			if (((l == x) || (l == 1)) && ((h == 1) || (h == y)))
				ft_putchar('o');
			(l > 1 && l < x && ((h == 1) || (h == y))) ? ft_putchar('-') : 0;
			(h > 1 && h < y && ((l == 1) || (l == x))) ? ft_putchar('|') : 0;
			(l > 1 && l < x && h > 1 && h < y) ? ft_putchar(' ') : 0;
			(l == x) ? ft_putchar('\n') : 0;
			l++;
		}
		l = 1;
		h++;
	}
}
